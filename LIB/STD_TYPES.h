#ifndef STD_TYPES_H
#define STD_TYPES_H

typedef unsigned char 		u8;
typedef unsigned short int  u16;
typedef unsigned int		u32;

typedef signed char 		s8;
typedef signed short int    s16;
typedef signed int		    s32;

typedef float				f32;
typedef double 				f64;


#define ENABLE              1
#define DISABLE             0

#define TRUE                1
#define FALSE               0

#define ON                  1
#define OFF                 0

#define NULL                ((void *)0)
#define RESET               0
#define SET                 1
#define ZERO                0

#endif